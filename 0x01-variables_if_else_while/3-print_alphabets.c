#include <stdio.h>
/**
 * main - Program that prints the alphabet in lowercase, and then in uppercase
 * You can only use the putchar
 * Return: Always 0 (Success)
 */
int main(void)
{
	char letter;

	for (letter = 'a'; letter <= 'z'; letter++)
		putchar(letter);
	for (letter = 'A'; letter <= 'Z'; letter++)
		putchar(letter);
	putchar('\n');
	return (0);
}
